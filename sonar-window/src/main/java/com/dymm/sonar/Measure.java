package com.dymm.sonar;

public class Measure {
	private double angle;
	private double distance;
	public Measure(double angle, double distance) {
		super();
		this.angle = angle;
		this.distance = distance;
	}
	public double getAngle() {
		return angle;
	}

	public double getDistance() {
		return distance;
	}
	
}
